#age wise

age = float (input("Enter Your Age : "))

if age<=14 :
    print("You are children")

elif age<=24 :
    print("Your age is Early working age")

elif age<=54:
    print("Your age is Prime working age")

elif age<=64 :
    print("Your age is Mature working age")
else :
    print("You are a Elderly")


